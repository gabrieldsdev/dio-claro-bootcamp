public class Operadores {
    public static void main(String[] args) {
        String nomeCompleto = "LINGUAGEM " + "JAVA";
        System.out.println(nomeCompleto);
        System.out.println("---------------");
        System.out.println(1+1+1+"1");
        System.out.println(1+"1"+1+1);
        System.out.println(1+"1"+1+"1");
        System.out.println("1"+1+1+1);
        System.out.println("1"+(1+1+1));
        System.out.println("---------------");
        int numero = 5;
        numero = - numero;
        System.out.println(numero);
        numero = numero * -1;
        System.out.println(numero);

    }
}
